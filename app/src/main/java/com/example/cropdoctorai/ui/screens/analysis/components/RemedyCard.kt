/**
 * Remedy section card with colored header bar and bullet-point items.
 * Uses dark glassmorphism background matching the app theme.
 * Used for Organic Remedies (green), Chemical Remedies (orange), and Prevention Tips (red).
 * All content is dynamically generated by Gemini AI — nothing is hardcoded.
 */
@Composable
fun RemedyCard(
    title: String,
    emoji: String,
    items: List<String>,
    headerColor: Color,
    bgColor: Color,
    modifier: Modifier = Modifier
) {
    // Dark glass tint of the header color for the content area
    val glassBg = headerColor.copy(alpha = 0.08f)

    Column(
        modifier = modifier
            .fillMaxWidth()
            .clip(RoundedCornerShape(14.dp))
            .border(1.dp, headerColor.copy(alpha = 0.25f), RoundedCornerShape(14.dp))
            .animateContentSize()
    ) {
        // Colored header bar
        Box(
            modifier = Modifier
                .fillMaxWidth()
                .background(headerColor)
                .padding(horizontal = 16.dp, vertical = 10.dp)
        ) {
            Row(
                verticalAlignment = Alignment.CenterVertically
            ) {
                Text(
                    text = emoji,
                    fontSize = 16.sp
                )
                Spacer(modifier = Modifier.width(8.dp))
                Text(
                    text = title,
                    style = MaterialTheme.typography.titleSmall,
                    color = Color.White,
                    fontWeight = FontWeight.Bold
                )
            }
        }

        // Content area — dark glass background
        Column(
            modifier = Modifier
                .fillMaxWidth()
                .background(glassBg)
                .padding(16.dp)
        ) {
            items.forEachIndexed { index, item ->
                Row(
                    modifier = Modifier.fillMaxWidth(),
                    verticalAlignment = Alignment.Top
                ) {
                    // Bullet dot
                    Box(
                        modifier = Modifier
                            .padding(top = 6.dp)
                            .size(6.dp)
                            .clip(CircleShape)
                            .background(headerColor)
                    )
                    Spacer(modifier = Modifier.width(10.dp))
                    Text(
                        text = item,
                        style = MaterialTheme.typography.bodySmall,
                        color = TextPrimary,
                        lineHeight = 18.sp,
                        modifier = Modifier.weight(1f)
                    )
                }
                if (index < items.lastIndex) {
                    Spacer(modifier = Modifier.height(10.dp))
                }
            }
        }
    }
}

/**
 * About disease section card with dark glass header and body.
 */
@Composable
fun AboutDiseaseCard(
    aboutText: String,
    modifier: Modifier = Modifier
) {
    Column(
        modifier = modifier
            .fillMaxWidth()
            .clip(RoundedCornerShape(14.dp))
            .border(1.dp, GlassBorder, RoundedCornerShape(14.dp))
    ) {
        // Header — dark glass
        Box(
            modifier = Modifier
                .fillMaxWidth()
                .background(Color.White.copy(alpha = 0.08f))
                .padding(horizontal = 16.dp, vertical = 10.dp)
        ) {
            Row(verticalAlignment = Alignment.CenterVertically) {
                Text(text = "📋", fontSize = 16.sp)
                Spacer(modifier = Modifier.width(8.dp))
                Text(
                    text = "ABOUT THIS DISEASE",
                    style = MaterialTheme.typography.labelMedium,
                    fontWeight = FontWeight.Bold,
                    color = TextSecondary,
                    letterSpacing = 0.5.sp
                )
            }
        }

        // Body — dark glass fill
        Box(
            modifier = Modifier
                .fillMaxWidth()
                .background(Color.White.copy(alpha = 0.04f))
                .padding(16.dp)
        ) {
            Text(
                text = aboutText,
                style = MaterialTheme.typography.bodyMedium,
                color = TextPrimary,
                lineHeight = 22.sp
            )
        }
    }
}
